# Считываем количество секунд, прошедших с начала суток
T = int(input())

# Вычисляем количество полных часов (в одном часе 3600 секунд)
h = (T // 3600) % 24

# Вычисляем количество полных минут, оставшихся после вычитания часов
m = (T % 3600) // 60

# Вычисляем количество оставшихся секунд
s = T % 60

# Если и минуты, и секунды меньше 10, добавляем ведущие нули к обоим
if s < 10 and m < 10:
    print(f"{h}:0{m}:0{s}")

# Если только секунды меньше 10 — добавляем ноль к секундам
elif s < 10:
    print(f"{h}:{m}:0{s}")

# Если только минуты меньше 10 — добавляем ноль к минутам
elif m < 10:
    print(f"{h}:0{m}:{s}")

# Если и минуты, и секунды больше или равны 10 — выводим как есть
else:
    print(f"{h}:{m}:{s}")


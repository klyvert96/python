# Считываем координаты первой клетки (строка и столбец)
a1 = int(input())
a2 = int(input())

# Считываем координаты второй клетки (строка и столбец)
b1 = int(input())
b2 = int(input())

# Определяем цвет первой клетки.
# Если сумма координат чётная — клетка белая, если нечётная — чёрная.
color_a = (a1 + a2) % 2

# Определяем цвет второй клетки по тому же принципу
color_b = (b1 + b2) % 2

# Проверяем, одинаковый ли цвет у клеток
if color_a == color_b:
    # Если одинаковый — выводим "Yes"
    print("Yes")
    
    # Дополнительно выводим цвет клеток
    if color_a == 0:
        # Если остаток 0, клетка белая
        print("White")
    else:
        # Если остаток 1, клетка чёрная
        print("Black")

# Если цвета разные — выводим "No"
else:
    print("No")



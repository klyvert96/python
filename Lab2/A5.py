# Считываем первое число A (с плавающей точкой)
a = float(input())

# Считываем второе число B (с плавающей точкой)
b = float(input())

# Проверяем, делится ли A на B без остатка
if a % b == 0:
    # Если делится нацело, выводим "YES"
    print("YES")
else:
    # Если не делится нацело, выводим "NO"
    print("NO")
